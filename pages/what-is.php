    <h3>Summary: A Complex Pattern</h3>

    <talk voice="echo">
    <p>In summary, a disorganized/fearful-avoidant attachment style involves:</p>

    <ul>
        <li>A painful internal conflict between craving and fearing intimacy</li>
        <li>Origins in unpredictable or traumatic childhood experiences</li>
        <li>A negative view of both self ("I'm unworthy") and others ("People will hurt me")<sup>1</sup></li>
        <li>Inconsistent behavior in relationships (pursuing then withdrawing)</li>
        <li>Difficulty with emotional regulation in close relationships</li>
        <li>A tendency to sabotage relationships when they become too intimate</li>
    </ul>

    <p>Understanding this attachment style is the first step toward healing. By recognizing these patterns, both those who have this attachment style and their loved ones can begin to make sense of seemingly contradictory behaviors and work toward more secure relationships.</p>

